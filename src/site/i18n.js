export function currentLanguage() {
  return localStorage.getItem(LANG_KEY) || "fr";
}

export function setLanguage(lang) {
  localStorage.setItem(LANG_KEY, dictionaries[lang] ? lang : "fr");
}

export function t(key) {
  const lang = currentLanguage();
  return dictionaries[lang]?.[key] || dictionaries.fr[key] || key;
}

export function tr(text) {
  const lang = currentLanguage();
  if (lang === "fr") return text;
  return textTranslations[lang]?.[text] || text;
}

export function translateDom(root = document) {
  const lang = currentLanguage();
  if (lang === "fr") return;
  root.querySelectorAll("input[placeholder], textarea[placeholder]").forEach((field) => {
    field.placeholder = tr(field.placeholder);
  });
  const walker = document.createTreeWalker(root, NodeFilter.SHOW_TEXT, {
    acceptNode(node) {
      if (!node.nodeValue.trim()) return NodeFilter.FILTER_REJECT;
      if (["SCRIPT", "STYLE"].includes(node.parentElement?.tagName)) return NodeFilter.FILTER_REJECT;
      return NodeFilter.FILTER_ACCEPT;
    },
  });
  const nodes = [];
  while (walker.nextNode()) nodes.push(walker.currentNode);
  nodes.forEach((node) => {
    const original = node.nodeValue;
    const trimmed = original.trim();
    const translated = tr(trimmed);
    if (translated !== trimmed) node.nodeValue = original.replace(trimmed, translated);
  });
}
